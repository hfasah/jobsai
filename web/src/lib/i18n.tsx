export const translations: Record<Locale, Messages> = { en, fr, de, es };

// ─── Context ───────────────────────────────────────────────────────────────────

type I18nCtx = {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string) => string;
};

const I18nContext = createContext<I18nCtx>({ locale: "en", setLocale: () => {}, t: (k) => k });

function detectLocale(): Locale {
  if (typeof window === "undefined") return "en";
  const cookie = document.cookie.split(";").find((c) => c.trim().startsWith(COOKIE + "="));
  if (cookie) {
    const val = cookie.split("=")[1]?.trim() as Locale;
    if (["en", "fr", "de", "es"].includes(val)) return val;
  }
  const lang = navigator.language.toLowerCase();
  if (lang.startsWith("fr")) return "fr";
  if (lang.startsWith("de")) return "de";
  if (lang.startsWith("es")) return "es";
  return "en";
}

function lookup(obj: unknown, keys: string[]): string | undefined {
  let cur = obj;
  for (const k of keys) {
    if (cur && typeof cur === "object") cur = (cur as Record<string, unknown>)[k];
    else return undefined;
  }
  return typeof cur === "string" ? cur : undefined;
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en");

  useEffect(() => {
    setLocaleState(detectLocale());
  }, []);

  const setLocale = (l: Locale) => {
    document.cookie = `${COOKIE}=${l};path=/;max-age=31536000;samesite=lax`;
    setLocaleState(l);
  };

  const t = (key: string): string => {
    const keys = key.split(".");
    return (
      lookup(translations[locale], keys) ??
      lookup(translations["en"], keys) ??
      key
    );
  };

  return <I18nContext.Provider value={{ locale, setLocale, t }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
